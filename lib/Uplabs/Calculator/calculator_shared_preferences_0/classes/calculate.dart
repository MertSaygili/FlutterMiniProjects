import 'package:dart_queue/dart_queue.dart';

class Calculate {
  double calculate(String text) {
    Queue<String> stack = Queue();

    for (int i = 0; i < text.length; i++) {
      switch (text[i]) {
        case '%':
        stack
          break;
        case '/':
          break;
        case 'X':
          break;
        case '-':
          break;
        case '+':
          break;
        case '.':
          break;
        default:
      }
    }
    return 0;
  }
}
